// tr returns the translated string for the given key
func tr(key string) string {
	if currentLanguage == "" {
		currentLanguage = "en"
	}
	t, ok := translations[currentLanguage]
	if !ok {
		t = translations["en"]
	}
	if v, ok := t[key]; ok {
		return v
	}
	if tEn, ok := translations["en"]; ok {
		if v, ok := tEn[key]; ok {
			return v
		}
	}
	return key
}
